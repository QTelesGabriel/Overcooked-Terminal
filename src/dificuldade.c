void escolher_dificuldade() {
    desenhar_tela();
    desenhar_escolhas_dif();
    noecho();
    char op;
    do {
        op = getch();
        switch (op) {
            case '1':
                quantidade_tripulantes = 3;
                tempo_de_jogo = 120;
                intervalo_pedidos = 6;
                break;
            
            case '2':
                quantidade_tripulantes =  4;
                tempo_de_jogo = 100;
                intervalo_pedidos = 4;
                break;
    
            case '3':
                quantidade_tripulantes = 5;
                tempo_de_jogo = 80;
                intervalo_pedidos = 3;
                break;
    
            default:
                break;
        }
    } while (op != '1' && op != '2' && op != '3');

    jogar();
}